/********************************************************

			 		是不是素数？

********************************************************/
#include<stdio.h>
int main()
{
	int shu,a;
	printf("请输入一个整数\n");
	scanf("%d",&shu);
	for(a=2;a<shu;a++)
	{
		if(shu%a==0)
		{
			printf("此数为合数\n");
			break;
		}
	}
	if(a==shu) printf("此数为素数\n");
}
