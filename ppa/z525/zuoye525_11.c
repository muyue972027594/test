/************************************************************

			本金、利率、年限，计算收益

*************************************************************/
#include<stdio.h>
int main()
{
	float bj,ll,nx,lx,money;
	printf("请依次输入本金、利率、年限(本金按元计算，利率按百分点计算)\n");
	scanf("%f%f%f",&bj,&ll,&nx);
	lx=bj*nx*ll/100;money=bj+lx;
	printf("最终获得的钱为%.2f元\n",money);
}
