/*******************************************************************

						计算税后工资

******************************************************************/
#include<stdio.h>
int main()
{
	float sh_q,sh_h;
	printf("请输入税前工资\n");
	scanf("%f",&sh_q);
	if(sh_q-3500>=0&&sh_q-3500<=1500) 
		sh_h=3500+(sh_q-3500)*0.97;
	else if(sh_q-3500>1500&&sh_q-3500<=4500) 
		sh_h=3500+(sh_q-3500)*0.9+105;
	else if(sh_q-3500>4500&&sh_q-3500<=9000) 
		sh_h=3500+(sh_q-3500)*0.8+555;
	else if(sh_q-3500>9000&&sh_q-3500<=35000) 
		sh_h=3500+(sh_q-3500)*0.75+1005;
	else if(sh_q-3500>35000&&sh_q-3500<=55000)  
		sh_h=3500+(sh_q-3500)*0.7+2755;
	else if(sh_q-3500>55000&&sh_q-3500<=80000)  
		sh_h=3500+(sh_q-3500)*0.65+5505;
	else if(sh_q-3500>80000)  
		sh_h=3500+(sh_q-3500)*0.55+13505;
	else
		sh_h=sh_q;
	printf("税后工资为%g元\n",sh_h);
}
