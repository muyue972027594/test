#include<stdio.h>
int main()
{
	int num_e;float num_m;
	printf("请输入电量\n");
	scanf("%d",&num_e);
	if(num_e>=0&&num_e<=170) num_m=num_e*0.477;
	else if(num_e>170&&num_e<=260) num_m=170*0.477+(num_e-170)*0.527;
	else if(num_e>260) num_m=170*0.477+90*0.527+(num_e-260)*0.777;
	printf("电费为%g\n",num_m);

}
