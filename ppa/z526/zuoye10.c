/***************************************************************

					三个数字交换

***************************************************************/
#include<stdio.h>
int main()
{
	float a,b,c,d;
	printf("请输入三个数字\n");
	scanf("%f%f%f",&a,&b,&c);
	printf("输入的三个数依次为%g,%g,%g\n",a,b,c);
	d=a;a=b;b=c;c=d;
	printf("交换后的三个数依次为%g,%g,%g\n",a,b,c);
}
